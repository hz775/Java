package com.bridgelabz.oops.leveltwo;

class Item {
    int idOfItem;
    String name;
    double price;
    int quantity;

    Item(int idOfItem, String name, double price, int quantity) {
        this.idOfItem = idOfItem;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
    }
}
